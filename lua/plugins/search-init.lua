-- =====================================================
-- 検索機能プラグイン統合設定
-- 各種検索プラグインを統合し、一元管理
-- =====================================================

-- このファイルは検索関連のプラグインを統合するエントリーポイント
-- 各プラグインは機能別に分割されており、必要に応じて有効/無効を切り替え可能

return {
  -- =====================================================
  -- 必須プラグイン（日常的に使用）
  -- =====================================================

  -- 高速ファイル・テキスト検索（メイン使用）
  -- パフォーマンス最優先で、ほぼ全ての検索ニーズをカバー
  require("plugins.search-finder"),

  -- 画面内高速ジャンプ（補助的使用）
  -- カーソル移動を劇的に効率化
  require("plugins.search-motion"),

  -- =====================================================
  -- オプションプラグイン（特定用途向け）
  -- =====================================================

  -- プロジェクト全体の検索置換（必要時のみ）
  -- リファクタリングや大規模な変更時に使用
  require("plugins.search-replace"),

  -- =====================================================
  -- 代替プラグイン（通常は無効）
  -- =====================================================

  -- Telescope（fzf-luaで不足する場合のみ有効化）
  -- 豊富な拡張機能が必要な場合にコメントアウトを解除
  -- require("plugins.search-telescope"),
}

-- =====================================================
-- 使い分けガイド
-- =====================================================
-- 1. 日常的な検索作業
--    → fzf-lua（search-finder.lua）
--    キー: <leader>f* でアクセス
--    用途: ファイル検索、grep、LSP機能など
--
-- 2. 画面内の移動
--    → flash.nvim（search-motion.lua）
--    キー: s, S でアクセス
--    用途: 見える範囲への高速ジャンプ
--
-- 3. 大規模な置換作業
--    → spectre.nvim（search-replace.lua）
--    キー: <leader>S でアクセス
--    用途: プロジェクト全体の一括置換
--
-- 4. 特殊な検索機能（通常不要）
--    → telescope.nvim（search-telescope.lua）
--    キー: <leader>t* でアクセス
--    用途: 特殊な拡張機能が必要な場合のみ
-- =====================================================